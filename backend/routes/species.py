"""
Species API routes for backend.

This module defines endpoints for retrieving all possible values of Species enum.

- GET /species/:
    List all possible animal species.
"""

#######################################################################################################################
# Imports
#######################################################################################################################

from fastapi import APIRouter

from backend.database.models import Species

#######################################################################################################################
# Globals
#######################################################################################################################

species_router = APIRouter()

#######################################################################################################################
# Body
#######################################################################################################################


@species_router.get(
    "",  # Explicitly set path to /species/
    summary="List all possible animal species",
    description="Returns all possible values for animal species as defined in the Species enum.",
    response_model=list[str],
)
def get_species():
    """Retrieve all possible animal species values."""
    return Species


