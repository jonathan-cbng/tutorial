"""
Sex API routes for backend.

This module defines endpoints for retrieving all possible values of SexEnum.

- GET /sex/:
    List all possible animal sexes.
"""

#######################################################################################################################
# Imports
#######################################################################################################################

from fastapi import APIRouter

from database.core.models import Sex

#######################################################################################################################
# Globals
#######################################################################################################################

sex_router = APIRouter()

#######################################################################################################################
# Body
#######################################################################################################################


@sex_router.get(
    "",  # Explicitly set path to /sex/
    summary="List all possible animal sexes",
    description="Returns all possible values for animal sex as defined in the SexEnum.",
    response_model=list[str],
)
def get_sexes():
    """Retrieve all possible animal sex values."""
    return Sex


